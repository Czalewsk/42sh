#include "libft.h"

int		ft_isflag(char c)
{
	if (c == ' ' || c == '+' || c == '-' || c == '#' || c == '0')
		return (1);
	return (0);
}

int		ft_isaconv(char c)
{
	if (c == '%')
		return (1);
	if (c == 's' || c == 'S' || c == 'c' || c == 'C')
		return (1);
	if (c == 'x' || c == 'X' || c == 'p')
		return (1);
	if (c == 'd' || c == 'D' || c == 'i')
		return (1);
	if (c == 'o' || c == 'O' || c == 'u' || c == 'U')
		return (1);
	return (0);
}
