#include "libft.h"

int		ft_rec_fac(int nb)
{
	if (nb == 0 || nb == 1)
		return (1);
	if (nb < 0 || nb > 12)
		return (0);
	else
		return (nb * ft_rec_fac(nb - 1));
}
