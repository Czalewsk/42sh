#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char	*dest2;

	dest2 = (unsigned char*)dest;
	while (n--)
		*dest2++ = *(unsigned char*)src++;
	return (dest);
}
